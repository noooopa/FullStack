package a0312;

public class VarEx3 {
    public static void main(String[] args) {
        // long 타입 변수 선언 및 초기화
        long longVar = 10000000000L;
        // boolean 타입 변수 선언 및 초기화
        boolean isTrue = true;

        // 출력
        System.out.println("long 타입 변수: " + longVar);
        System.out.println("boolean 타입 변수: " + isTrue);
    }
}
